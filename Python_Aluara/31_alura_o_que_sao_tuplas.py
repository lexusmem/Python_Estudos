# lista e string são sequencias
lista = [1, 2, 3, 4, 5]
palavra = 'banana'

print(len(lista))
print(min(palavra))
print(lista[0])
print(palavra[1])

# Lista são mutáveis
dias = ["S", "T", "Q", "Q", "S", "S", "D"]
print(dias)
type(dias)
# Tuplas são sequencias imutáveis e de dados exclusivos.
dias_semana = {"S", "T", "Q", "Q", "S", "S", "D"}
print(dias_semana)
type(dias_semana)
