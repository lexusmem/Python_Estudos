# IF = SE
# ELIF - else if - Se não
# ELSE - senão, sem resposta
# Papai você comprou pão?
# Se sim, ele agradece
# Se não, ele chora


reposta = input('# Papai você comprou pão? ')

if reposta == 'Sim':
    print('Obrigado Papai!')
elif reposta == 'Não':
    print('Estou chorando!')
else:
    print('Não foi o que eu perguntei.')
