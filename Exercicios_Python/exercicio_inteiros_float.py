numero_1 = int(input('Digite o primero numero inteiro: '))
numero_2 = int(input('Digite o segundo numero inteiro: '))
numero_3 = float(input('Digite o primeiro numero float: '))

# O produto do dobro do primeiro com metade do segundo
resultado_1 = (numero_1 * 2) * (numero_2 / 2)

print(f'Resultado 1: {resultado_1}')

# Soma do triplo do primeiro com o terceiro
resultado_2 = (numero_1 * 3) + numero_3

print(f'Resultado 2: {resultado_2}')

# O terceiro elevado ao cubo
resultado_3 = numero_3 ** 3

print(f'Resultado 3:{resultado_3: .2f}')
