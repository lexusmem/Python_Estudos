# Linguagens formais e naturais

# Linguagem naturais são os idiomas que as pessoas falam como o português.

# Linguagem formais são criadas pelas pessoas para aplicações especificas.
# Exemplo: Químicos usam uma linguagem formal para representar a estrutura
# química de uma molécula, H2O.

# As linguagens de computadores são idiomas formais criados para expressar
# operações com computadores.

# Cada linguagem formal possui sua sintaxe e estrutura de declaração.

# As linguagens formais são criadas para não possuir ambiguidade e para ser
# literais tendo o significado exatamente iguais ao que é expressado.

